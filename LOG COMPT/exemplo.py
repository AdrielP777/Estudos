# Entradas possíveis (valores booleanos)
A = True
B = False
# Porta AND
print("AND:", A and B) # False
# Porta OR
print("OR:", A or B) # True
# Porta NOT (inverte o valor)
print("NOT A:", not A) # False
print("NOT B:", not B) # True
# Porta NAND
print("NAND:", not (A and B)) # True
# Porta NOR
print("NOR:", not (A or B))
# Porta XOR (A diferente de B)
# False
print("XOR:", A!= B) # True
# Porta XNOR (A igual a В)
print("XNOR:", A == B) # False